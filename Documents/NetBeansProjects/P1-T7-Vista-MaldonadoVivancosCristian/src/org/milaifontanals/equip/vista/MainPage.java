    //busquem l'id de la categoria seleccionada
    private int idCategoria(String nom){
        List<Categoria> cats=Constants.getCategs();
        int id=-1,i=0, len=cats.size();
        boolean found=false;
        do{
            if(nom.contains(cats.get(i).getNom())){
                id=cats.get(i).getId();
            }
            i++;
        }while(i<len || found);
        return id;
    }
